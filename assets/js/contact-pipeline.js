let lastAutoGeneratedSubject = '';
let subjectWasManuallyEdited = false;
let subjectInternalUpdate = false;
let isSubmitting = false;

function setFormOpenedAt() {
  if (formOpenedAt) formOpenedAt.value = String(Date.now());
}

